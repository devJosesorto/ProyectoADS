    public static void crearPDF() throws FileNotFoundException, DocumentException {

        // Se crea el documento
        Document documento = new Document();

        // El OutPutStream para el fichero donde crearemos el PDF
        FileOutputStream ficheroPDF = new FileOutputStream("Personas.pdf");

        // Se asocia el documento de OutPutStream
        PdfWriter.getInstance(documento, ficheroPDF);

        // Se abre el documento
        documento.open();

        // Parrafo
        Paragraph titulo = new Paragraph("NOMBRE DE LA EMPRESA \n\n\n",
                FontFactory.getFont("arial",
                        15,
                        Font.BOLD,
                        BaseColor.BLACK
                )
        );

        PdfPTable codigo = new PdfPTable(2);
        codigo.addCell("Comprobante de retencion IVA 1%");
        codigo.addCell("                        CR09900");

        Paragraph detalle = new Paragraph("\n\n"
                + "FECHA: 30 DE SEPTIEMBRE DEL 2020\n"
                + "CLIENTE: MEGA SERVI, S.A. DE C.V.\n"
                + "DIRECCION: KM26 1/2 CANTON LOURDES, HDA PASATIEMPO\n"
                + "NIT:0210-140305-101-0\n"
                + "NCR164232-1\n"
                + "GIRO:ACTIVIDADES INMOBILIARIAS \n\n\n\n",
                FontFactory.getFont("arial",
                        11,
                        Font.PLAIN,
                        BaseColor.BLACK
                )
        );

        PdfPTable descripcion = new PdfPTable(4);
        descripcion.addCell("CANTIDAD");
        descripcion.addCell("                           DESCRIPCION");
        descripcion.addCell("   PRECIO UNITARIO");
        descripcion.addCell("   MOONTO SUJETO A RETENCION");

        // AÃ±adimos el titulo al documento
        documento.add(titulo);
        documento.add(codigo);
        documento.add(detalle);
        documento.add(descripcion);

        // Se cierra el documento
        documento.close();
    }

